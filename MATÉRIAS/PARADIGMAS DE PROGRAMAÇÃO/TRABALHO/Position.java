package TRABALHO;

public class Position {
    // Atributos
    int lin;
    int col;
    
    // Construtor
    public Position(int lin, int col) {
        this.lin = lin;
        this.col = col;
    }

    // Getters e Setters
    public int getLin() {
        return lin;
    }

    public void setLin(int lin) {
        this.lin = lin;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }
}
