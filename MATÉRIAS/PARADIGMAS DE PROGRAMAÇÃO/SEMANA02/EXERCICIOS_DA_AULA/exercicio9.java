public class exercicio9 {
    
    public static void main(String[] args) {
        
        int i, j;
        int[][] m = {{1,2}, {3,4}, {5, 6}};

        for (i = 0; i < 3; i++){
            for (j = 0; j < 2; j++){
                System.out.print(m[i][j] + " ");
            }
        }

        System.out.print("\n");
    }
}
